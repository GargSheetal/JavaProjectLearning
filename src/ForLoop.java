
public class ForLoop {

	public static void main(String[] args) {
		
		int i=1;
		
		//print 1-10 
		for(i=1; i<=10; i++)
		{
			System.out.println(i);
		}
		
		//print 10-1
		for(i=10; i>=1; i--)
		{
			System.out.println(i);
		}
		
		//print even num bet 1-10
		for(i=2; i<=10; i+=2)
		{
			System.out.println(i);
		}
		
		//print multiples of 3 till 30
		for(i=3; i<=30; i+=3)
		{
			System.out.println(i);
		}
		
		
		
	}
}
